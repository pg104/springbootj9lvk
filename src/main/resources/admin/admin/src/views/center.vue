      if((!this.ruleForm.zhanghao)&& 'zuozhe'==this.flag){
        this.$message.error('账号不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'zuozhe'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if((!this.ruleForm.xingming)&& 'zuozhe'==this.flag){
        this.$message.error('姓名不能为空');
        return
