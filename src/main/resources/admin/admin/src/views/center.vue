    return {
      ruleForm: {},
      flag: '',
      usersFlag: false,
      zuozhexingbieOptions: [],
      zhuanjiaxingbieOptions: [],
      bianjixingbieOptions: [],
    };
  },
  mounted() {
    var table = this.$storage.get("sessionTable");
    this.flag = table;
    this.$http({
      url: `${this.$storage.get("sessionTable")}/session`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.ruleForm = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
    this.zuozhexingbieOptions = "男,女".split(',')
    this.zhuanjiaxingbieOptions = "男,女".split(',')
    this.bianjixingbieOptions = "男,女".split(',')
  },
