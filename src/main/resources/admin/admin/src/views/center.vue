        this.$message.error(`邮箱应输入邮箱格式`);
        return
      }
      if( 'zuozhe' ==this.flag && this.ruleForm.shoujihaoma&&(!isMobile(this.ruleForm.shoujihaoma))){
        this.$message.error(`手机号码应输入手机格式`);
        return
      }
        if(this.ruleForm.xiangpian!=null) {
                this.ruleForm.xiangpian = this.ruleForm.xiangpian.replace(new RegExp(this.$base.url,"g"),"");
        }
      if((!this.ruleForm.zhuanjiazhanghao)&& 'zhuanjia'==this.flag){
        this.$message.error('专家账号不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'zhuanjia'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if((!this.ruleForm.zhuanjiaxingming)&& 'zhuanjia'==this.flag){
        this.$message.error('专家姓名不能为空');
        return
      }
      if( 'zhuanjia' ==this.flag && this.ruleForm.youxiang&&(!isEmail(this.ruleForm.youxiang))){
        this.$message.error(`邮箱应输入邮箱格式`);
        return
      }
      if( 'zhuanjia' ==this.flag && this.ruleForm.lianxishouji&&(!isMobile(this.ruleForm.lianxishouji))){
        this.$message.error(`联系手机应输入手机格式`);
        return
      }
        if(this.ruleForm.xiangpian!=null) {
                this.ruleForm.xiangpian = this.ruleForm.xiangpian.replace(new RegExp(this.$base.url,"g"),"");
        }
      if((!this.ruleForm.bianjizhanghao)&& 'bianji'==this.flag){
        this.$message.error('编辑账号不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'bianji'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if((!this.ruleForm.bianjixingming)&& 'bianji'==this.flag){
        this.$message.error('编辑姓名不能为空');
        return
      }
      if( 'bianji' ==this.flag && this.ruleForm.youxiang&&(!isEmail(this.ruleForm.youxiang))){
        this.$message.error(`邮箱应输入邮箱格式`);
        return
      }
      if( 'bianji' ==this.flag && this.ruleForm.shoujihaoma&&(!isMobile(this.ruleForm.shoujihaoma))){
        this.$message.error(`手机号码应输入手机格式`);
        return
      }
        if(this.ruleForm.xiangpian!=null) {
                this.ruleForm.xiangpian = this.ruleForm.xiangpian.replace(new RegExp(this.$base.url,"g"),"");
        }
      if('users'==this.flag && this.ruleForm.username.trim().length<1) {
	this.$message.error(`用户名不能为空`);
        return	
      }
      this.$http({
        url: `${this.$storage.get("sessionTable")}/update`,
        method: "post",
        data: this.ruleForm
      }).then(({ data }) => {
        if (data && data.code === 0) {
          this.$message({
            message: "修改信息成功",
            type: "success",
            duration: 1500,
            onClose: () => {
            }
          });
        } else {
          this.$message.error(data.msg);
        }
      });
    }
